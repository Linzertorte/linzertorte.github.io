const zeroPad = (num, places) => String(num).padStart(places, '0')

var pic_prefix = "larousse-";
if(!$("#page_num").is(":hidden")) {
    pic_prefix = "m-larousse-";
}

$("#btn").on("click", function (e) {
    var word =  document.getElementById("word").value;
    word = clean_word(word);
    var i = 0;
    for(i = 0; i < words.length; i += 1) {
        words[i] = clean_word(words[i]);
    }
    i = 0
    while((i < words.length) && (words[i]=='$' || words[i]<=word)) {
        i+=1;
    }
    if(i>0) i-=1;
    while (i>=0 && words[i]=='$'){
        i-=1;
    }
    cur_page = i;
    var html = "<img src=\""+pic_prefix+zeroPad(cur_page + 1,4)+".png\">"
    $("#page").html(html)
    $("#page_num").text(cur_page + 1);
});
$("#prev").on("click", function (e) {
    if(cur_page > 0) {
        cur_page -= 1;
        var html = "<img src=\""+pic_prefix+zeroPad(cur_page + 1,4)+".png\">"
        $("#page").html(html)
        $("#page_num").text(cur_page + 1);
    }
});
$("#next").on("click", function (e) {
    if(cur_page < words.length - 1){
        cur_page += 1;
        var html = "<img src=\""+pic_prefix+zeroPad(cur_page + 1,4)+".png\">"
        $("#page").html(html)
        $("#page_num").text(cur_page + 1);
    }
});

